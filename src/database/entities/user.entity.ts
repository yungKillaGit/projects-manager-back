import { Column, Entity, Unique } from "typeorm";

import { BaseModel } from "./base-model.entity";

@Entity('users')
export class User extends BaseModel {
  @Column({ length: 100 })
  name: string;

  @Column()
  password: string;

  @Unique('username', ['username'])
  @Column({ length: 200 })
  username: string;

  @Column('simple-array')
  roles: string[];

  @Column()
  isAccountDisabled: boolean;

  @Unique('email', ['email'])
  @Column({ length: 200 })
  email: string;
}
